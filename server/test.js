function blockDrawer(grid) {
    console.log(grid.length);
    for (var i = 0; i < grid.length; i++) {
        for (var j = 0; j < grid.length; j++) {
            if (grid[i][j] === 1) {
                context2.drawImage(block, x * j, x * i);
            }
        }
    }
}
